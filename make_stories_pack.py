def expand():
    questions=[]
    ctx_codes = {"в классе":"CLS","в кружке":"CRG","с друзьями":"FRD","дома":"HOME"}
    for base in S:
        for ctx, intro_ctx in contexts:
            qid = f"ST{base['base_id']}_{ctx_codes[ctx]}"
            text = base["text"].replace("{ctx}", ctx)
            intro = f"{intro_ctx}. " + base["intro"]
            questions.append({
                "id": qid,
                "format": "single",
                "text": text,
                "text_child": text,
                "text_teen": text,
                "intro_child": intro,
                "intro_teen": intro,
                "options": base["options"],
                "tags": ["style:story","aud:child","aud:teen","aud:adult"]
            })
    return questions

questions = expand()

