# py39_fix_unions.py Р Р†Р вЂљРІР‚Сњ Р В Р’В·Р В Р’В°Р В РЎВР В Р’ВµР В Р вЂ¦Р РЋР РЏР В Р’ВµР РЋРІР‚С™ "Type | None" Р В Р вЂ¦Р В Р’В° "Optional[Type]" Р В РЎвЂ Р В РўвЂР В РЎвЂўР В Р’В±Р В Р’В°Р В Р вЂ Р В Р’В»Р РЋР РЏР В Р’ВµР РЋРІР‚С™ Р В РЎвЂР В РЎВР В РЎвЂ”Р В РЎвЂўР РЋР вЂљР РЋРІР‚С™ Optional
import pathlib, re

p = pathlib.Path("src/main.py")
src = p.read_text(encoding="utf-8")

# 1) Р В Р’В·Р В Р’В°Р В РЎВР В Р’ВµР В Р вЂ¦Р В РЎвЂР РЋРІР‚С™Р РЋР Р‰ Р В Р вЂ Р В РЎвЂўР В Р’В·Р В Р вЂ Р РЋР вЂљР В Р’В°Р РЋРІР‚В°Р В Р’В°Р В Р’ВµР В РЎВР РЋРІР‚в„–Р В Р’Вµ Р РЋРІР‚С™Р В РЎвЂР В РЎвЂ”Р РЋРІР‚в„–: "-> Something | None" -> "-> Optional[Something]"
src2 = re.sub(r'->\s*([\w\.]+)\s*\|\s*None', r'-> Optional[\1]', src)

# 2) Р В Р вЂ¦Р В Р’В° Р В Р вЂ Р РЋР С“Р РЋР РЏР В РЎвЂќР В РЎвЂР В РІвЂћвЂ“ Р РЋР С“Р В Р’В»Р РЋРЎвЂњР РЋРІР‚РЋР В Р’В°Р В РІвЂћвЂ“: Р В РЎвЂ”Р В Р’ВµР РЋР вЂљР В Р’ВµР В РЎВР В Р’ВµР В Р вЂ¦Р В Р вЂ¦Р РЋРІР‚в„–Р В Р’Вµ/Р В РЎвЂ”Р В Р’В°Р РЋР вЂљР В Р’В°Р В РЎВР В Р’ВµР РЋРІР‚С™Р РЋР вЂљР РЋРІР‚в„– Р В Р вЂ Р В РЎвЂР В РўвЂР В Р’В° ": Something | None" -> ": Optional[Something]"
src2 = re.sub(r':\s*([\w\.]+)\s*\|\s*None', r': Optional[\1]', src2)

# 3) Р В РўвЂР В РЎвЂўР В Р’В±Р В Р’В°Р В Р вЂ Р В РЎвЂР РЋРІР‚С™Р РЋР Р‰ Р В РЎвЂР В РЎВР В РЎвЂ”Р В РЎвЂўР РЋР вЂљР РЋРІР‚С™ Optional, Р В Р’ВµР РЋР С“Р В Р’В»Р В РЎвЂ Р В Р’ВµР В РЎвЂ“Р В РЎвЂў Р В Р вЂ¦Р В Р’ВµР РЋРІР‚С™
if "from typing import Optional" not in src2:
    src2 = "from typing import Optional\n" + src2

if src2 != src:
    p.write_text(src2, encoding="utf-8")
    print("OK: src/main.py Р В РЎвЂ”Р РЋР вЂљР В РЎвЂР В Р вЂ Р В Р’ВµР В РўвЂР РЋРІР‚ВР В Р вЂ¦ Р В РЎвЂќ Р РЋР С“Р В РЎвЂР В Р вЂ¦Р РЋРІР‚С™Р В Р’В°Р В РЎвЂќР РЋР С“Р В РЎвЂР РЋР С“Р РЋРЎвЂњ Python 3.9 (Optional[..]).")
else:
    print("Р В Р’ВР В Р’В·Р В РЎВР В Р’ВµР В Р вЂ¦Р В Р’ВµР В Р вЂ¦Р В РЎвЂР В РІвЂћвЂ“ Р В Р вЂ¦Р В Р’ВµР РЋРІР‚С™ Р Р†Р вЂљРІР‚Сњ Р РЋРІР‚С›Р В Р’В°Р В РІвЂћвЂ“Р В Р’В» Р РЋРЎвЂњР В Р’В¶Р В Р’Вµ Р РЋР С“Р В РЎвЂўР В Р вЂ Р В РЎВР В Р’ВµР РЋР С“Р РЋРІР‚С™Р В РЎвЂР В РЎВ.")
