
from typing import Dict, Optional
from .models import SessionState, QuestionBank
def apply_weights(st, weights, factor=1.0):

    if not hasattr(st, "vectors"):
        st.vectors = {}

    for key, value in weights.items():

        if not isinstance(value, (int, float)):
            continue

        st.vectors[key] = (
            st.vectors.get(key, 0.0)
            + value * factor
        )
def pick_result(vectors: dict):
    """
    Возвращает отсортированные векторы.
    Код индотипа теперь определяется через indotype_resolver.
    """
    totals = {k: float(vectors.get(k, 0.0)) for k in ["G","S","T","K"]}
    ordered = sorted(totals.items(), key=lambda kv: kv[1], reverse=True)
    return ordered

def next_question_id(state:SessionState, bank:QuestionBank)->Optional[str]:
    asked = set(state.asked)
    for q in bank.questions:
        if q.id not in asked:
            return q.id
    return None
def is_finished(state:SessionState, bank:QuestionBank)->bool:
    return next_question_id(state, bank) is None


