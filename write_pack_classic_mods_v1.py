def add_block(aud, prefix, block):
    for i,(t,intro,opts) in enumerate(block, start=1):
        packs[aud].append(q(f"CL-{aud.upper()}-{prefix}{i:02d}", aud, t, intro, opts))

for aud in ["child","teen","adult"]:
    add_block(aud,"EO-",EO)
    add_block(aud,"CP-",CP)
    add_block(aud,"BP-",BP)

for aud,data in packs.items():
    name=f"pack_classic_mods_{aud}_v1.json"
    (P/name).write_text(json.dumps({"questions":data},ensure_ascii=False,indent=2),encoding="utf-8")
    print(f"OK: {name} → {len(data)} вопросов")
