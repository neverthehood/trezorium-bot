def q(id, aud, title, intro, opts):
    return {"id":id,"format":"single",
            f"text_{'child' if aud=='child' else 'teen' if aud=='teen' else ''}".strip("_"):title,
            f"intro_{'child' if aud=='child' else 'teen' if aud=='teen' else ''}".strip("_"):intro,
            "options":opts, "tags":[f"aud:{aud}","style:classic","quality:rich"]}

def opt(letter, label, w, m): 
    o={"id":letter}
    if label: o["label"]=label
    o["weights"]=w
    if m: o["mods"]=m
    return o

